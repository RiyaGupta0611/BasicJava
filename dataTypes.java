
package JavaBasics;

public class dataTypes {
    public static void main(String[] args) {
        int a = 21;              
        float b = 21.66f;            
        double d = 31.66666;      
        char e = 'R';               
        boolean f = false;          

        System.out.println("This is an Integer " + a);
        System.out.println("This is a Float " + b);
        System.out.println("This is a Double " + d);
        System.out.println("This is a Char " + e);
        System.out.println("This is a Boolean " + f);
    }
}